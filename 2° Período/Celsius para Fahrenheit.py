#6. Converter Celsius para Fahrenheit 
#Escreva um programa que converta uma temperatura de graus Celsius para Fahrenheit.
#Formula do calculo de Celsius para Fahrenheit
#F = C * 1,8 + 32

c = int(input('Quantos graus celsius? '))
f = (c * 1.8) +32
print(f'{c} graus celsius equivalem a {f} fahrenheit')